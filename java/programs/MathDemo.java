//Math class

class MathDemo
{
	public static void main(String arg[])
	{
		System.out.println("Math.PI = " + Math.PI);
		System.out.println("Math.E = " + Math.E);

		System.out.println("Math.abs(-16) = " + Math.abs(-16));

		System.out.println("Math.ceil(12.54) = " + Math.ceil(12.54));

		System.out.println("Math.floor(12.95) = " + Math.floor(12.95));

		System.out.println("Math.exp(1) = " + Math.exp(1));

		System.out.println("Math.exp(2) = " + Math.exp(2));

		System.out.println("Math.log(10) = " + Math.log(10));

		System.out.println("Math.max(16, 20) = " + Math.max(16,20));

		System.out.println("Math.min(15, 18) = " + Math.min(15, 18));

		System.out.println("Math.pow(3, 4) = " + Math.pow(3, 4));

		System.out.println("Math.random() = " + Math.random());

		System.out.println("Math.rint(13.85) = " + Math.rint(13.85));

		System.out.println("Math.rint(13.45) = " + Math.rint(13.45));

		System.out.println("Math.round(13.75) = " + Math.round(13.75));

		System.out.println("Math.round(13.05) = " + Math.round(13.05));

		System.out.println("Math.sqrt(9) = " + Math.sqrt(9));

	}
}
